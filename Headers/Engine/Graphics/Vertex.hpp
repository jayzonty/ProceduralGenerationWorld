#pragma once

namespace Engine
{
/**
 * Vertex class
 */
struct Vertex
{
    /**
     * X-coordinate
     */
    float x;

    /**
     * Y-coordinate
     */
    float y;

    /**
     * Red color component
     */
    float r;

    /**
     * Green color component
     */
    float g;

    /**
     * Blue color component
     */
    float b;

    /**
     * Alpha color component
     */
    float a;

    /**
     * Texture u-coordinate
     */
    float u;

    /**
     * Texture v-coordinate
     */
    float v;
};
}
